    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        double patrimonio;
        double pago;
        int estrato;
        String nombre;
        String inscripcion;
        patrimonio=Double.parseDouble(txtPat.getText());
        estrato=Integer.parseInt(txtEst.getText());
        nombre=txtNom.getText();
        inscripcion=txtIns.getText();
        if (patrimonio > 2000000 && estrato > 3){
            pago= (0.03 *patrimonio)+50000;   
        }else{
            pago=50000;   
        }
        
        txtNom2.setText(String.valueOf(nombre));
        txtIns2.setText(String.valueOf(inscripcion));
        txtPag.setText(String.valueOf(pago));
    }//GEN-LAST:event_jButton1ActionPerformed

